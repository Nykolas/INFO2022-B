# CON NUMERAL SE ESCRIBEN LOS COMENTARIOS

# TIPOS DE DATOS

# NUMERICO - ENTERO (int)
num = 10
print(f"La variable num, contiene el valor: {num} y es del tipo {type(num)}")
# NUMERICO - DECIMALES (float)
num_decimales = 3.14
print(f"La variable num_decimales, contiene el valor: {num_decimales} y es del tipo {type(num_decimales)}")
# ALFANUMERICO (string)
palabra = 'Nicolas'
print(f"La variable palabra, contiene el valor: {palabra} y es del tipo {type(palabra)}")
# Logico
band = True # Verdadero
band = False # Falso
print(f"La variable band, contiene el valor: {band} y es del tipo {type(band)}")

# SOn ESTRUCTURAS COMPLEJAS
# Listas
ejemplo_lista = [10,100,250,'Nicolas']
print(f"La variable ejemplo_lista, contiene el valor: {ejemplo_lista} y es del tipo {type(ejemplo_lista)}")
# Tuplas
ejemplo_tupla = (10,100,250,'Nicolas')
print(f"La variable ejemplo_tupla, contiene el valor: {ejemplo_tupla} y es del tipo {type(ejemplo_tupla)}")
# Diccionarios
ejemplo_diccionario = {'nombre':'Nicolas', 'apellido':'Tortosa'}
print(f"La variable ejemplo_diccionario, contiene el valor: {ejemplo_diccionario} y es del tipo {type(ejemplo_diccionario)}")


